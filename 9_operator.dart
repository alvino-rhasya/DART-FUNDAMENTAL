void main(List<String> args) {
  // Operator aritmatika
  var a = 5;
  var b = 2;

  print(a + b); //Penjumlahan
  print(a - b); //Pengurangan
  print(a * b); //Perkalian
  print(a / b); //Pembagian
  print(a % b); //Modulus
  print(a ~/ b); // Pembagian yang dibulatkan

  // icrement & Decrement
  var c = 10;
  var d = 5;
  c++; // c = c/10 + 1
  c--; // d = d/5 - 1

  print(c); // output 11
  print(d); // output 4

  var f = 0;
  f += 5; // output = 5
}