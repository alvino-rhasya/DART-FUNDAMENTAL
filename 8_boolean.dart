void main(List<String> args) {
  // !   => Kebalikan
  // =   => adalah
  // ==  => Membandingkan Sama
  // === => Membandingkan Sama indentik (Sama persis)
  // !=  => Membandingkan Tidak sama dengan
  // !== => Membandingkan tidak sama identik (jarang dipakai)
  var isTrue = true;
  var isFalse = false;

  print(isTrue);
  print(!isFalse); // ! membuat nilai boolean menjadi lawannya/kebalikannya

  print(isTrue != false);
}